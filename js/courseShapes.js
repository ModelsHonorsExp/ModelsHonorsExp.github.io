game.checkLocation = function(z, x) {
    let posVec = new SAT.Vector(z, x);
    if(SAT.pointInCircle(posVec, this.greenShape)) {
        return GREEN;
    }
    for(let i = 0; i < this.courseShapes.length; i++) {
        if(SAT.pointInPolygon(posVec, this.courseShapes[i])) {
            return FAIRWAY;
        }
    }
    return ROUGH;
}
